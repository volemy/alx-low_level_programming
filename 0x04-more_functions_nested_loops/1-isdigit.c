/**
 * _isdigit - function that checks for digits 0 through 9
 * @c: is character to be checked
 *
 * Description: should check for digits 0 through 9
 *
 * Return: 1 if character is a digit, 0 otherwise
 */
int _isdigit(int c)
{

if (c >= 48 && c <= 57)

return (1);

else

return (0);

}

